phrase = "Hello.I'm excited to learn Python"
print(phrase.lower()) #lower case string
print(phrase.upper()) #upper case string
print(phrase.isupper()) #boolean, if string is upper case
print(phrase.upper().isupper())
print(len(phrase)) #length of the string
print(phrase[3]) #select the string number of the phrase,starts with 0
print(phrase.index("Python")) #location number of the string of the phrase
print(phrase.replace("Hello", "Jambo")) #replacing string